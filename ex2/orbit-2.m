x=[5.764 6.282 6.759 7.168 7.408]
y=[0.648 1.202 1.823 2.526 3.360]
A=[x(1)^2 x(1)*y(1) y(1)^2 x(1) y(1);
    x(2)^2 x(2)*y(2) y(2)^2 x(2) y(2);
    x(3)^2 x(3)*y(3) y(3)^2 x(3) y(3);
    x(4)^2 x(4)*y(4) y(4)^2 x(4) y(4);
    x(5)^2 x(5)*y(5) y(5)^2 x(5) y(5)]
%X=[a b c d e]';
b=[-1 -1 -1 -1 -1]';
a=inv(A)*b
c=[a(1),a(2);a(2),a(3)]; 
d=[a(1),a(2),a(4);a(2),a(3),a(5);a(4),a(5),1]; 
d0=det(d); 
c0=eig(c); 
d0=abs(d0); 
c1=abs(det(c)); 
r1=abs(c0(1)); 
r2=abs(c0(2)); 
a=d0/(c1*r1); 
b=d0/(c1*r2); 
t=0:0.1:(2*pi+0.1); 
x=a*cos(t); 
y=b*sin(t); 
